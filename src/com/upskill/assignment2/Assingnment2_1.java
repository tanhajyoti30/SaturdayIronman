//Write a for loop that prints 50 through 1 separated by spaces in the same line.
public class Assingnment2_1 {

	public static void main(String[] args) {
		
		loops();
	}
	
	public static void loops() {
		for( int i = 50; i >= 1; i--) {
		
			System.out.print("  "+ i);
			
		}
		
		}
	}


	
